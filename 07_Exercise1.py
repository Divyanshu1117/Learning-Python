n = int(input("Enter First Number:=> "))
k = int(input("Enter Second Number:=> "))

result1 = n + k
print("Addition:=>", result1)

result2 = n - k
print("Subtraction:=>", result2)

result3 = n * k
print("Multi:=>", result3)

result4 = n**k
print("Exponentiation:=>", result4)

result5 = n / k
print("Division:=>", result5)

result6 = n // k
print("Floor Division:=>", result6)

result7 = n % k
print("Modulus:=>", result7)